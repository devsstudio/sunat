export function getDepts(): UbigeoDept[] {
    var depts = Object.entries(NESTED_UBIGEOS).reduce<UbigeoDept[]>((acc: UbigeoDept[], cur: [string, Table13Dept]) => {
        acc.push({
            dept_code: cur[0],
            dept_name: cur[1].dept_name
        });
        return acc;
    }, []);
    depts.sort((a: UbigeoDept, b: UbigeoDept) => (a.dept_name > b.dept_name ? 1 : -1));
    return depts;
}

